#include <CLed.h>
#include <MyLed.h>
#include "Bsp.h"

/*********************************************************************************************************************/
/*-------------------------------------------------Global variables--------------------------------------------------*/
/*********************************************************************************************************************/
CLed myLed1(&MODULE_P00, 5, true);                                                               /* On-board LED1    */
CLed myLed2(&MODULE_P00, 6, true);                                                               /* On-board LED2    */

/*********************************************************************************************************************/
/*---------------------------------------------Function Implementations----------------------------------------------*/
/*********************************************************************************************************************/
/* Function to turn ON the LED1 */
void TurnOnLed1(void)
{
    myLed1.TurnOn();
}

/* Function to turn ON the LED2 */
void TestLeds(void)
{
    // Toggle LED 1 (On --> Off)
    myLed1.Toggle();

    // Toggle LED 2 (Off --> On)
    myLed2.Toggle();

    // Wait 200 ms
    waitTime(200);

    // Toggle LED 2 (On --> Off)
    myLed2.Toggle();

    // Toggle LED 1 (Off --> On)
    myLed1.Toggle();

    // Wait 200 ms
    waitTime(200);
}
